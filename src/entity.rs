//! Entity identity infrastructure.
//!
//! [`Entity`] — core trait every entity type implements.
//! [`TrackedFor`] — companion trait linking `Tracked*` structs back to their entity type.
//! [`EntityRef`] — typed reference (id + optional parent chain).
//! [`EntityKind`] — discriminant enum for all entity types.
//! [`StoreEntity`] — store-level enum wrapping all tracked entity types.
//! [`ParentKind`] — sealed marker for parent type parameters.

use std::marker::PhantomData;

