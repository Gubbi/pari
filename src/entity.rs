// ---------------------------------------------------------------------------
// Entity trait
// ---------------------------------------------------------------------------

pub trait Entity: Sized + 'static {
    const KIND: EntityKind;
    fn validation_schema() -> &'static ValidationSchema<Self>;
    type Parent: ParentKind;
    type Tracked: TrackedFor<Entity = Self>;

    /// Convert a typed EntityRef to the type-erased AnyEntityRef.
    /// Generated by `#[derive(Entity)]`.
    fn to_any_ref(entity_ref: &EntityRef<Self, Self::Parent>) -> AnyEntityRef;

    /// Extract this entity's tracked form from a StoreEntity.
    /// Returns None if the variant doesn't match.
    /// Generated by `#[derive(Entity)]`.
    fn extract(entity: &StoreEntity) -> Option<&Self::Tracked>;
}

// ---------------------------------------------------------------------------
// TrackedFor companion trait
// ---------------------------------------------------------------------------

/// Implemented by every tracked entity struct.
/// Provides the roundtrip: `TrackedRole::Entity = Role`
/// and therefore `Role::Tracked::Entity = Role`.
pub trait TrackedFor {
    type Entity: Entity<Tracked = Self>;
}

// ---------------------------------------------------------------------------
// Tracked<T> type alias
// ---------------------------------------------------------------------------

/// Ergonomic alias: `Tracked<Role>` instead of `<Role as Entity>::Tracked`.
pub type Tracked<T> = <T as Entity>::Tracked;

// ---------------------------------------------------------------------------
// EntityRef<T, P>
// ---------------------------------------------------------------------------

/// A typed reference to an entity: its string id and optional parent context.
///
/// `T: Entity` ensures only declared entity types can form refs.
/// `P = NoParent` default covers top-level entities.
///
/// Hash and Eq incorporate `T::KIND` so refs of different entity types with
/// the same id do not collide as HashMap keys.
pub struct EntityRef<T: Entity, P: ParentKind = NoParent> {
    pub(crate) id: String,
    pub(crate) parent: P,
    _marker: PhantomData<fn() -> T>,
}

impl<T: Entity> EntityRef<T, NoParent> {
    /// Construct a top-level entity reference.
    pub fn new(id: impl Into<String>) -> Self {
        Self { id: id.into(), parent: NoParent, _marker: PhantomData }
    }
}

impl<T: Entity> EntityRef<T, WorkflowParent> {
    /// Construct an embedded entity reference with its parent workflow id.
    pub fn new_embedded(id: impl Into<String>, workflow_id: impl Into<String>) -> Self {
        Self {
            id: id.into(),
            parent: WorkflowParent { workflow_id: workflow_id.into() },
            _marker: PhantomData,
        }
    }
}

impl<T: Entity, P: ParentKind> EntityRef<T, P> {
    pub fn id(&self) -> &str {
        &self.id
    }

    pub fn parent(&self) -> &P {
        &self.parent
    }
}

impl<T: Entity, P: ParentKind + PartialEq> PartialEq for EntityRef<T, P> {
    fn eq(&self, other: &Self) -> bool {
        self.id == other.id && self.parent == other.parent
    }
}

impl<T: Entity, P: ParentKind + Eq> Eq for EntityRef<T, P> {}

impl<T: Entity, P: ParentKind + std::hash::Hash> std::hash::Hash for EntityRef<T, P> {
    fn hash<H: std::hash::Hasher>(&self, state: &mut H) {
        T::KIND.hash(state);
        self.id.hash(state);
        self.parent.hash(state);
    }
}

impl<T: Entity, P: ParentKind + Clone> Clone for EntityRef<T, P> {
    fn clone(&self) -> Self {
        Self { id: self.id.clone(), parent: self.parent.clone(), _marker: PhantomData }
    }
}

impl<T: Entity, P: ParentKind + std::fmt::Debug> std::fmt::Debug for EntityRef<T, P> {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        f.debug_struct("EntityRef")
            .field("kind", &T::KIND)
            .field("id", &self.id)
            .field("parent", &self.parent)
            .finish()
    }
}

// ---------------------------------------------------------------------------
// EntityRef Serialize / Deserialize
// ---------------------------------------------------------------------------

impl<T: Entity> serde::Serialize for EntityRef<T, NoParent> {
    fn serialize<S: serde::Serializer>(&self, s: S) -> Result<S::Ok, S::Error> {
        use serde::ser::SerializeMap;
        let mut map = s.serialize_map(Some(2))?;
        map.serialize_entry("id", &self.id)?;
        map.serialize_entry("kind", T::KIND.as_str())?;
        map.end()
    }
}

impl<T: Entity> serde::Serialize for EntityRef<T, WorkflowParent> {
    fn serialize<S: serde::Serializer>(&self, s: S) -> Result<S::Ok, S::Error> {
        use serde::ser::SerializeMap;
        let mut map = s.serialize_map(Some(3))?;
        map.serialize_entry("id", &self.id)?;
        map.serialize_entry("kind", T::KIND.as_str())?;
        map.serialize_entry("workflow_id", &self.parent.workflow_id)?;
        map.end()
    }
}

impl<'de, T: Entity> serde::Deserialize<'de> for EntityRef<T, NoParent> {
    fn deserialize<D: serde::Deserializer<'de>>(d: D) -> Result<Self, D::Error> {
        use serde::de::{MapAccess, Visitor};
        use std::fmt;

        struct V<T>(std::marker::PhantomData<T>);

        impl<'de, T: Entity> Visitor<'de> for V<T> {
            type Value = EntityRef<T, NoParent>;

            fn expecting(&self, f: &mut fmt::Formatter) -> fmt::Result {
                write!(f, "an entity ref object")
            }

            fn visit_map<A: MapAccess<'de>>(
                self,
                mut map: A,
            ) -> Result<EntityRef<T, NoParent>, A::Error> {
                let mut id: Option<String> = None;
                while let Some(key) = map.next_key::<String>()? {
                    match key.as_str() {
                        "id" => id = Some(map.next_value()?),
                        _ => { let _: serde_json::Value = map.next_value()?; }
                    }
                }
                let id = id.ok_or_else(|| serde::de::Error::missing_field("id"))?;
                Ok(EntityRef::new(id))
            }
        }

        d.deserialize_map(V::<T>(std::marker::PhantomData))
    }
}

impl<'de, T: Entity> serde::Deserialize<'de> for EntityRef<T, WorkflowParent> {
    fn deserialize<D: serde::Deserializer<'de>>(d: D) -> Result<Self, D::Error> {
        use serde::de::{MapAccess, Visitor};
        use std::fmt;

        struct V<T>(std::marker::PhantomData<T>);

        impl<'de, T: Entity> Visitor<'de> for V<T> {
            type Value = EntityRef<T, WorkflowParent>;

            fn expecting(&self, f: &mut fmt::Formatter) -> fmt::Result {
                write!(f, "an embedded entity ref object")
            }

            fn visit_map<A: MapAccess<'de>>(
                self,
                mut map: A,
            ) -> Result<EntityRef<T, WorkflowParent>, A::Error> {
                let mut id: Option<String> = None;
                let mut workflow_id: Option<String> = None;
                while let Some(key) = map.next_key::<String>()? {
                    match key.as_str() {
                        "id" => id = Some(map.next_value()?),
                        "workflow_id" => workflow_id = Some(map.next_value()?),
                        _ => { let _: serde_json::Value = map.next_value()?; }
                    }
                }
                let id = id.ok_or_else(|| serde::de::Error::missing_field("id"))?;
                let workflow_id =
                    workflow_id.ok_or_else(|| serde::de::Error::missing_field("workflow_id"))?;
                Ok(EntityRef::new_embedded(id, workflow_id))
            }
        }

        d.deserialize_map(V::<T>(std::marker::PhantomData))
    }
}


